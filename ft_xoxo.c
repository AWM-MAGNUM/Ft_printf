#include "ft_printf.h"

int    ft_xoxo(unsigned long int nb, char *t, int *k)
{
    if(nb >= 16)
         ft_xoxo((nb / 16), t, k);
    *k += ft_putchar(t[nb%16]);
    return (*k);
}